//if and if-else
let a = 2;
let b = 4;
if (a > b) {
  console.log("a is greater than b");
} else {
  console.log("a is less than b");
}
// if..else if...else
let c = 2;
let d = 4;
if (c > d) {
  console.log("c is greater than d");
} else if (c == d) {
  console.log("c is equal to d");
} else {
  console.log("c is less than d");
}
